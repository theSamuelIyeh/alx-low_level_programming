#include "3-calc.h"

/**
 * op_add - add two integers
 * @a: first integer
 * @b: second integer
 * Return: addition of the two integers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two integers
 * @a: first integer
 * @b: second integer
 * Return: subtract of the two integers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two integers
 * @a: first integer
 * @b: second integer
 * Return: multiplication of the two integers
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two integers
 * @a: first integer
 * @b: second integer
 * Return: division of the two integers
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - divide two integers and get the remainder
 * @a: first integer
 * @b: second integer
 * Return: modulo of the two integers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
